
public class Main {
    public static void main(String[] args) {

        for (int i=3; i<=35; i+=6) {
            System.out.println(i);
        }
        System.out.println("--------");

        int i=3;
        do {
            System.out.println(i);
            i+=6;
        } while (i <= 35);

        System.out.println("--------");

        int n = 3;
        while (n <= 35) {
           System.out.println(n);
           n+=6;

        }
        System.out.println("--------");


    }
}
